    document.addEventListener("DOMContentLoaded", function () {
        var enquiryModal = new bootstrap.Modal(document.getElementById('enquiryModal'));
        enquiryModal.show();

        $('#enquiryForm').on('submit', function (e) {
            e.preventDefault(); // Prevent form from reloading page

            var formData = $(this).serialize();

            $.ajax({
                url: 'enquire_now.php',
                type: 'POST',
                data: formData,
                dataType: 'json',
                beforeSend: function () {
                    Swal.fire({
                        title: 'Processing...',
                        text: 'Please wait while we send your enquiry.',
                        allowOutsideClick: false,
                        didOpen: () => {
                            Swal.showLoading();
                        }
                    });
                },
                success: function (response) {
                    if (response.status === 'success') {
                        Swal.fire({
                            icon: 'success',
                            title: '‚úÖ Enquiry sent successfully!',
                            confirmButtonText: 'OK'
                        }).then(() => {
                            enquiryModal.hide(); // Modal hide after successful submission
                        });
                        $('#enquiryForm')[0].reset();
                    } else {
                        Swal.fire({
                            icon: 'error',
                            title: '‚ùå Email sending failed!',
                            text: response.message,
                            confirmButtonText: 'Try Again'
                        });
                    }
                },
                error: function () {
                    Swal.fire({
                        icon: 'error',
                        title: '‚ùå Something went wrong!',
                        text: 'Please try again later.',
                        confirmButtonText: 'OK'
                    });
                }
            });
        });
    });
